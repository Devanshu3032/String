public class IndexOfAndCompareTo {
    public static void main(String[] args) {
        String s = "Devansshu Kumar" ;
        String ss = "Dav" ;
        System.out.println(s.indexOf('s'));
        System.out.println(s.lastIndexOf('s'));
         
        System.out.println(s.compareTo(ss));

    }
}
